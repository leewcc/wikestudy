package com.wikestudy.model.pojo;

public class Record {
	private int id;
	private int userId;
	private String name;
	private long studyTime;
	private int disNum;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getStudyTime() {
		return studyTime;
	}
	public void setStudyTime(long studyTime) {
		this.studyTime = studyTime;
	}
	public int getDisNum() {
		return disNum;
	}
	public void setDisNum(int disNum) {
		this.disNum = disNum;
	}
	
	
}
