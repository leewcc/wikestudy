package com.wikestudy.model.pojo;

public class Power {
	
	private int powId;				//权限id
	private String powDes;		//权限描述
	public void setPowId(int powId) {
		this.powId = powId;
	}
	public void setPowDes(String powDes) {
		this.powDes = powDes;
	}
	public int getPowId() {
		return powId;
	}
	public String getPowDes() {
		return powDes;
	}
	
	
}
